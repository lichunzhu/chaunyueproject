package com.sample;


public class Album {

  private long id;
  private String uid;
  private long pictureId;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getUid() {
    return uid;
  }

  public void setUid(String uid) {
    this.uid = uid;
  }


  public long getPictureId() {
    return pictureId;
  }

  public void setPictureId(long pictureId) {
    this.pictureId = pictureId;
  }

}
