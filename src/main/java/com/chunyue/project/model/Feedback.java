package com.sample;


public class Feedback {

  private long id;
  private String uid;
  private String mess;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getUid() {
    return uid;
  }

  public void setUid(String uid) {
    this.uid = uid;
  }


  public String getMess() {
    return mess;
  }

  public void setMess(String mess) {
    this.mess = mess;
  }

}
